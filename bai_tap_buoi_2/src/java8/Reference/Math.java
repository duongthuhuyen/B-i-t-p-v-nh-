package java8.Reference;

public class Math{
    public static int add(int a,int b){
        return a+b;
    }
    public static  int sub(int a,int b){
        return  a-b;
    }
    public static  int nhan(int a,int b){
        return  a*b;
    }
    public static int chia(int a,int b){
        return (int) ((double) a/b);
    }

}
